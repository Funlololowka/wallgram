interface PixelIconProps {
  name: PixelIconName;
  size?: number;
  className?: string;
}

export function PixelIcon({ name, size = 18, className }: PixelIconProps) {
  const rows = ICONS[name];
  const pixels: Array<{ x: number; y: number }> = [];

  rows.forEach((row, y) => {
    row.split("").forEach((cell, x) => {
      if (cell === "1") pixels.push({ x, y });
    });
  });

  return (
    <svg
      className={className ? `pixel-icon ${className}` : "pixel-icon"}
      width={size}
      height={size}
      viewBox="0 0 8 8"
      aria-hidden="true"
      focusable="false"
    >
      {pixels.map((pixel) => (
        <rect key={`${pixel.x}-${pixel.y}`} x={pixel.x} y={pixel.y} width="1" height="1" />
      ))}
    </svg>
  );
}
